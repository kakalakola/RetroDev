;Bits used to check specific input
.define _CTRL_01_SELECT %0010000000000000
.define _CTRL_01_L      %0000000000000000
.define _CTRL_01_R      %0000000000000000
.define _CTRL_01_B      %1000000000000000
.define _CTRL_01_UP     %0000100000000000
.define _CTRL_01_DOWN   %0000010000000000
.define _CTRL_01_LEFT   %0000001000000000
.define _CTRL_01_RIGHT  %0000000100000000
                       ;%0000000000000000
                        ;|:|:|:|:|:|:|:|:
                        ;|:|:|:|:|:|:Controller Identifier
                        ;|:|:|:|:|:|R
                        ;|:|:|:|:|:L
