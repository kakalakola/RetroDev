vBlank:
  php
  phy
  phx
  pha

  rep #$30                  ;A/X/Y are 16-bit

  ldx.w #dmaVBlank
  ldy.w #$4300
  lda.w #$80
  mvn $80,$80

  lda #_VBLANK_DMA_START
  sta $420b

  ;Check controller 01
  ;Invert bits, which makes it possible to check multiple bits at once
  ;Store data to RAM

  ;Check to see if joypad auto-read has finished reading controller data. If not, wait.
-:lda $4212
  bit #%00000000
       ;|:|:|:|:
       ;|:|:|:|Joypad auto-read (done|not done)
       ;|:xxxxx
       ;|hBlank flag
       ;vBlank flag
  bne -

  ldx ctrl01

  lda $4218                 ;Read input from controller port 01
  bit #$0f                  ;Check bits 3-0
  bne noController          ;If the result is NOT zero, then whatever's plugged in to the control port 
                            ;is not an SNES controller

  eor #$ffff                ;Flip the bits. This helps later on with button+d-pad inputs

  ;AND only produces 1 if both bits being compared are 1. So AND between ctrl01 and ctrl01Hold is a good way to see if the direction or button was continuously being held
  sta ctrl01
  txa
  and ctrl01
  sta ctrl01Hold

  ;Where as an EOR only produces 1 if one of the bits is 1 and the other is 0. So an EOR between ctrl01 and ctrl01Hold only results in a bit being 1 if the button was NOT previously held down.
  txa
  eor ctrl01Hold
  sta ctrl01Toggle
  jmp +

noController:
  lda #$ffff
  sta ctrl01
  sta ctrl01Hold

+:lda $4210                 ;Read NMI register to clear NMI/VBlank flag
  ;lda $4211                 ;Read Video IRQ register to clear IRQ flag
  pla
  plx
  ply
  plp
  rti

