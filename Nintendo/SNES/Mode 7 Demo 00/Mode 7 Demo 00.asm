;.TABLE and .ROW makes it *somewhat* easier to manage data blocks. Instead of having to wrangle multiple .DB and .DW lines, data for a single DMA configuration can be neatly defined in a single line/row

dmaInitRAM0:
  .table byte,byte,word,byte,word,byte,word,word,word,word
  ;DMA 0: RAM clear 01 - $7E0000-$7EFFFF                                       (Padding)
  .row _DMA_CFG_RAM,$80,zeroFill,$80,0                                        ,0,0,0,0,0
  ;DMA 1: RAM clear 02 - $7F0000-$7FFFFF
  .row _DMA_CFG_RAM,$80,zeroFill,$80,0                                        ,0,0,0,0,0
  ;DMA 2: CRAM clear
  .row _DMA_CFG_RAM,$22,zeroFill,$80,0                                        ,0,0,0,0,0
  ;DMA 3: VRAM clear
  .row _DMA_CFG_VRAM,$18,zeroFill,$80,0                                       ,0,0,0,0,0
  ;DMA 4: Copy palette (CRAM write address should be pointing to $0000
  .row _DMA_BYTE_WRITE,$22,palette,$80,(paletteEnd-palette)                   ,0,0,0,0,0
  ;DMA 5: Initialize variables in RAM
  .row _DMA_BYTE_WRITE,$80,initVariables,$80,(initVariablesEnd-initVariables) ,0,0,0,0,0

  ;So ... this is where things get *weird*. Mode 7 stores its data in an interleaved format of TN PD TN PD format. The first byte (TN) is the Tile Number, which references one of the 256 available tiles. The second byte (PD) is Pixel Data, and it directly referencing one of the 256 colors in the palette.

  ;Since the VRAM write register is set to increment after writing to $2119, tile data for Mode 7 can be copied to VRAM without any further register modifications. :D

dmaInitRAM1:
  ;DMA 0: Copy Mode 7 Tiles (VRAM write address should be pointing to $0000)
  .row _DMA_BYTE_WRITE,$19,mode7Tile,$80,(mode7TileEnd-mode7Tile)             ,0,0,0,0,0
  ;DMA 1: Point PPU write address to $0000
  .row _DMA_WORD_WRITE,$16,zeroFill,$80,2                                     ,0,0,0,0,0
  ;DMA 2: Configure PPU write address to increment after writes to $2118
  .row _DMA_BYTE_WRITE,$15,ppuWriteConfig,$80,1                               ,0,0,0,0,0
  ;DMA 3: Copy Mode 7 map
  .row _DMA_BYTE_WRITE,$18,mode7Map,$80,(mode7MapEnd-mode7Map)                ,0,0,0,0,0
  ;DMA 4: Configure DMA tile map mode
  .row _DMA_BYTE_WRITE,$1a,mode7MapConfig,$80,1                               ,0,0,0,0,0
  ;DMA 5: Re-Configure PPU write address to increment after writes to $2119
  .row _DMA_BYTE_WRITE,$15,(ppuWriteConfig+1),$80,1                           ,0,0,0,0,0

dmaVBlank:                  ;DMA to run during vBlank
  ;DMA 0: Update BG 0 scroll registers
  .row _DMA_DOUBLE_WRITE,$0d,scrollX,$80,4                                    ,0,0,0,0,0
  ;DMA 1: Update Mode 7 Matrix registers A & B
  .row _DMA_DOUBLE_WRITE,$1b,mode7A,$80,4                                     ,0,0,0,0,0
  ;DMA 2: Update Mode 7 Matrix registers C & D
  .row _DMA_DOUBLE_WRITE,$1d,mode7C,$80,4                                     ,0,0,0,0,0
  ;DMA 3: Update Mode 7 Center X & Y (not used)
  .row _DMA_DOUBLE_WRITE,$1f,mode7CenterX,$80,4                               ,0,0,0,0,0
dmaVBlankEnd:


