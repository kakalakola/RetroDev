;**NOTE**
;Instructions such as BIT, ADC, and SBC are 8-bit operations by default, and need to be appeneded with a ".W". Without it, and error is generated and the CPU jumps to emptyHandler.

inputHandler:
  ;Check if the select button is pressed. If so, reset position & scale.
  lda ctrl01Toggle
  bit.w #_CTRL_01_SELECT
  beq noReset
  inc $60

  ;Reset everything in scratch data by way of MVN
  ldx.w #resetVariables
  ldy.w #scrollXScratchLo
  lda.w #$10
  mvn $80,$80

  jmp inputCheckDone        ;Nothing more to do...

noReset:
  lda ctrl01                ;Load controller input
  tax                       ;Create a backup copy in X
  ldy #0

  ;Check if the B button is pressed. if not, the section to update scale values can be skipped
  bit.w #_CTRL_01_B
  bne checkUp

  bit.w #(_CTRL_01_B+_CTRL_01_UP) ;If the B button IS pressed, check if B+Up is pressed
  bne checkBDown

  sec
  lda scaleXScratchLo
  sbc.w scaleXSpeed
  sta scaleXScratchLo
  lda scaleXScratchHi
  sbc.w #0
  sta scaleXScratchHi

  sec
  lda scaleYScratchLo
  sbc scaleYSpeed
  sta scaleYScratchLo
  lda scaleYScratchHi
  sbc.w #0
  sta scaleYScratchHi

  and.w #$ff00              ;A little something to ensure the scale doesn't to into negative values
  beq shortHop              ;inputCheckDone is too "far" for BEQ to branch to. Hence the short hop to JMP

  ldy #0
  lda scaleXSpeed
  sta scaleXScratchLo
  lda scaleYSpeed
  sta scaleYScratchLo
  sty scaleXScratchHi
  sty scaleYScratchHi

shortHop:
  jmp inputCheckDone

checkBDown:
  txa
  bit.w #(_CTRL_01_B+_CTRL_01_DOWN)
  bne inputCheckDone

  clc
  lda scaleXScratchLo
  adc.w scaleXSpeed
  sta scaleXScratchLo
  lda scaleXScratchHi
  adc.w #0
  sta scaleXScratchHi

  clc
  lda scaleYScratchLo
  adc.w scaleYSpeed
  sta scaleYScratchLo
  lda scaleYScratchHi
  adc.w #0
  sta scaleYScratchHi

  jmp inputCheckDone

checkUp:
  txa
  bit.w #_CTRL_01_UP
  bne checkDown

  clc
  lda scrollYScratchLo
  adc scrollYSpeed
  sta scrollYScratchLo
  lda scrollYScratchHi
  adc.w #0
  sta scrollYScratchHi
  jmp checkRight            ;If up IS pressed, there's no need to test down ...?

checkDown:
  txa
  bit.w #_CTRL_01_DOWN
  bne checkRight

  sec
  lda scrollYScratchLo
  sbc scrollYSpeed
  sta scrollYScratchLo
  lda scrollYScratchHi
  sbc.w #0
  sta scrollYScratchHi
  
checkRight:
  txa
  bit.w #_CTRL_01_RIGHT
  bne checkLeft

  sec
  lda scrollXScratchLo
  sbc scrollXSpeed
  sta scrollXScratchLo
  lda scrollXScratchHi
  sbc.w #0
  sta scrollXScratchHi
  jmp inputCheckDone

checkLeft:
  txa
  bit.w #_CTRL_01_LEFT
  bne inputCheckDone        ;Technically an RTL can be used here

  clc
  lda scrollXScratchLo
  adc scrollXSpeed
  sta scrollXScratchLo

  lda scrollXScratchHi
  adc.w #0
  sta scrollXScratchHi

inputCheckDone:
  rtl                       ;ReTurn from subroutine, Long

