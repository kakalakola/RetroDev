  ;Using emptyHandler as a catch-all of sorts. In theory, the only interrupt addresses used by this code are "reset" and "vBlank". This sets a value in $70, and effectively halts the CPU. If that happens, something's probably wrong with the code
emptyHandler:
  sep #$20                  ;A is 8-bit
  stz $4200                 ;Disable NMI
  inc $40
  wai
  jmp emptyHandler          ;Loop back to emptyHandler if the CPU is restarted, somehow

