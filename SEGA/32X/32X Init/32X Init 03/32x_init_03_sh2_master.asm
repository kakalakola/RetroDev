;Master SH2 code
  org $06000000

  ;Jump table $000000-$000120
  dc.l masterColdStart      ;Location of start code for cold start    $000000
  dc.l SH2_M_STACK          ;Stack pointer for cold start             $000004
                            ;set to $0603f000-$0603ffff
  dc.l error                ;Location of start code for manual reset  $000008
  dc.l SH2_M_STACK          ;Stack pointer for manual reset           $00000c
                            ;(Same as cold reset...)

  dc.l error                ;Illegal Instruction                      $000010
  dc.l $00000000            ;Reserved                                 $000014
  dc.l error                ;Invalid Slot Instruction                 $000018
  dc.l $20100400            ;System Reserved (ICE Vector)             $00001c
  dc.l $20100420            ;System Reserved (ICE Vector)             $000020
  dc.l error                ;CPU Address Error                        $000024
  dc.l error                ;DMA Address Error                        $000028
  dc.l error                ;NMI                                      $00002c
  dc.l error                ;User Break                               $000030

  dcb.l 19,$00000000        ;Reserved                         $000034-$00007f
  dcb.l 32,error            ;Trap vectors                     $000080-$0000ff

  ;Interrupt vectors. From the looks of things, byte 0 doesn't seem to do much in the 32X.

  dc.l error                ;IRQ 1                                    $000100
  dc.l error                ;IRQ 2 & 3                                $000104
  dc.l error                ;IRQ 4 & 5                                $000108
  dc.l error                ;PWM interrupt (IRQ 6 & 7)                $00010c
  dc.l error                ;Command interrupt (IRQ 8 & 9)            $000110
  dc.l error                ;HBlank interrupt (IRQ 10 & 11)           $000114
  dc.l VBlank               ;VBlank interrupt (IRQ 12 & 13)           $000118
  dc.l error                ;VRes interrupt (IRQ 14 & 15)             $00011c

  ;Start of code at $000120
masterColdStart:

  ;**NOTE**
  ;The SH2 only supports 8-bit values for immediate MOV instructions, i.e. MOV #$E2,Rn
  ;The assembler deals with 16-bit and 32-bit immediate MOV instructions by adding the number at the end of the ROM, and then loading the data by reference. i.e. MOV #$0100,Rn results in $0100 being added to the end of ROM, and the instruction MOV addressOf0100,Rn being used instead.

  ;I prefer to manually specify data blocks at the end of code. But from a programming POV, there's nothing particularly wrong with MOV #$20004000,R15 :)

  mov SH2_M_STACK,r15      ;Load stack address for master to r15
  mov CT_SYS_REG_ADDR,r14 ;Load cache through address for system register to r14
  
  ldc r14,gbr               ;LoaD to Control register
                            ;Load the address stored in r14 to Global Base Register

  ;Doing things properly now, waiting for the Slave SH2 and Genesis to signal they're ready before doing anything else.
  ;$40xx-$40yy is a 4-byte block of address used by the Slave SH2 to communicate with the Master SH2

waitForSlaveSH2:
  mov @($24,gbr),r0
  mov STAT_CHECK,r1       ;"S_OK"
  cmp/eq r1,r0              ;CoMPare if the value in R0 is EQual to value in R1
  bf waitForSlaveSH2        ;Branch if False to waitForSlaveSH2

waitForGenesis:
  mov.l @($28,gbr),r0
  mov.l STAT_CHECK+4,r1
  cmp/eq r1,r0
  bf waitForGenesis

  ;Disable interrupts
  ;**NOTE**
  ;There seems to be an address/range limit to MOV.W
  ; MOV.W INT_DISABLE,R0 (or MOV.W #$8000,R0) *only* works so long as INT_DISABLE  is within a certain range. This wasn't an issue with previous demos as the ROM size was small. But I ran into issues when adding line table & pixel data to the end of this project.

  mov INT_DISABLE,r0
  mov.w r0,@(0,gbr)         ;Write #$8000 to GBR ($4000) to disable interrupts
  mov.w r0,@($0100,gbr)     ;Write #$8000 to $4100 to disable video

  ;Clear interrupt flags. Even though they're not all used, it's good practice

  mov #0,r0

  mov.w r0,@($14,gbr)       ;Clear VRES interrupt
  mov.w r0,@($14,gbr)
  mov.w r0,@($16,gbr)       ;Clear VBlank
  mov.w r0,@($16,gbr)
  mov.w r0,@($18,gbr)       ;Clear HBlank
  mov.w r0,@($18,gbr)
  mov.w r0,@($1a,gbr)       ;Clear CMD interrupt
  mov.w r0,@($1a,gbr)
  mov.w r0,@($1c,gbr)       ;Clear PWM interrupt
  mov.w r0,@($1c,gbr)

  ;Copy palette data to color RAM ($00004200) with the ~power~ of DMA
  ;There are a different kind of DMAs that can be done. For this demo, the focus is on 32X RAM > 32X RAM (color palette & frame buffer) DMA.
  mov DMA_BASE_ADDR,r14
  ldc r14,gbr               ;Point GBR to DMA base address of $FFFFFF80

  mov PALETTE_DATA,r0       ;R0==paletteDataData
  mov r0,@(0,gbr)           ;$FFFFFF80==DMA 0 source address

  mov CT_PALETTE_ADDR,r0    ;R0==$00004200, DMA destination
  mov r0,@(4,gbr)           ;$FFFFFF84==DMA 0 destination address

  mov.b #32,r0              ;Amount of "units" to copy. Each color consists of 16 bits, so 32 colors to copy, basically
                            ;If the transfer size is defined as 4, or 16, the number is rounded down to the nearest multiple of 4. #16-#19 copies 16*4=64 bytes of data. #20 will result in 80 bytes of data being copied.
  mov r0,@(8,gbr)           ;$FFFFFF88==DMA 0 copy amount (in words)

  mov R2R_TRANSFER_SIZE_16,r0 ;Unit size in bytes
                            ;Although TRANSFER_SIZE_16 seems to function like TRANSFER_SIZE_4, 16-byte writes use both DMA channels. Byte 1 is set upon completion of the DMA.
  mov r0,@($c,gbr)          ;$FFFFFF8c==DMA 0 control code

  mov.b #1,r0
  mov r0,@($30,gbr)         ;Writing #1 to $FFFFFFB0 enables the DMA, on both channels if they're set
                            ;As long as the value at $FFFFFB0==1, writing a non-zero value to DMA amount, then changing byte 1 of DMA_TRANSFER_SIZE_n to 0 will automatically start the next DMA.

  ;Copy line table & pixel data to frame buffer 1
  mov F_BUFFER_DATA,r0
  mov r0,@($10,gbr)
  mov CT_F_BUFFER_ADDR,r0
  mov r0,@($14,gbr)
  mov F_BUFFER_DATA_SIZE,r0
  mov r0,@($18,gbr)
  mov R2R_TRANSFER_SIZE_16,r0
  mov r0,@($1c,gbr)

  ;Since DMA 0 and DMA 1 run without interfering with each other. To copy pixel data to frame buffer using DMA 0 again, we need to make sure the data transfer has completed.

  mov.b #3,r1               ;The last 2 bits of $FFFFFF8C need to be checked
loopWaitDMA0:
  ;mov.b @($f,gbr),r0        ;Works in MAME, but causes Gens & Fusion to glitch?
  ;mov.w @($e,gbr),r0        ;Works in MAME, but causes Gens & Fusion to glitch?
  mov @($c,gbr),r0
  and r1,r0                 ;Mask out all the unnecessary bits
  cmp/eq r1,r0              ;See if the last 2 bits are set, eg R0==3
  bf loopWaitDMA0

  mov.b #3,r1               ;The last 2 bits of $FFFFFF9C need to be checked
loopWaitDMA1:
  mov @($1c,gbr),r0 
  and r1,r0
  cmp/eq r1,r0
  bf loopWaitDMA1

  ;Set GBR to system register
  mov CT_SYS_REG_ADDR,r14
  ldc r14,gbr

  ;Swap frame buffer        ;Make frame buffer 1 the active frame buffer
  mov F_BUFFER_SWAP,r0
  mov.w r0,@($010a,gbr)

  ;Enable display
  mov VIDEO_ENABLE,r0
  mov.w r0,@($0100,gbr)

  ;Enable VBlank
  mov INT_ENABLE,r0
  mov.w r0,@(0,gbr)         ;Write to $20004000

  mov SYS_REG_IRQ_EN,r0
  ldc r0,sr                 ;Write to System Register, which controls IRQ masking

mainLoop:
  mov LOOP_COUNTER,r0       ;Load an address (non-cache-through) into R0
  mov @r0,r1                ;Load the value at the address in R0 into R1
  add #1,r1                 ;Add #1 to the value in R1
  mov r1,@r0                ;Store the value in R1 back into the address at R0

  bra mainLoop
  nop                       ;Because of how the SH2 handles things, BRA needs to be followed with a NOP for safety
  .align 4

VBlank:
  mov VBLANK_COUNTER,r0     ;Load an address (non-cache-through) into R0
  mov @r0,r1                ;Load the value at the address in R0 into R1
  add #1,r1                 ;Add #1 to the value in R1
  mov r1,@r0                ;Store the value in R1 back into the address at R0

  mov CT_SYS_REG_ADDR,r14
  ldc r14,gbr

  ;Clear VInt flag
  mov #0,r0
  mov.w r0,@($16,gbr)       ;Write twice to register $4016, V interrupt clear
  mov.w r0,@($16,gbr)

  rte                       ;ReTurn from intErrupt
  nop
  .align 4

error:
  bra error
  nop
  .align 4

