                             ;|:|:|:|:|:|:|:FEN, Frame buffer access ENabled, (access allowed|access denied)
                             ;|:|xxxxxxxxxxx
                             ;|:PEN, Palette access ENabled (access allowed|access denied)
                             ;|HBlank (currently in HBlank|currently not in HBlank)
                             ;VBlank (currently in VBlank|currently not in VBlank)


;System register, written to SR
SYS_REG_IRQ_EN:         dc.l %0000000010110000
                             ;|:|:|:|:|:|:|:|:
                             ;|:|:|:|:|:|:|:|T bit
                             ;|:|:|:|:|:|:|:S bit
                             ;|:|:|:|:|:|:Always set to 0
                             ;|:|:|:|:IRQ Mask (Uses 4 bit number to determine highest level, like 68K)
                             ;|:|:|:|Q Bit
                             ;|:|:|:M Bit
                             ;Always set to 0
;**NOTE**
;An IRQ mask of %1011 will trigger all IRQs *higher* than %1011 (11), so IRQs 12, 13, 14, and 15

SH2_M_STACK:            dc.l $0603f000

CT_SYS_REG_ADDR:        dc.l $20004000
CT_PALETTE_ADDR:        dc.l $20004200
CT_F_BUFFER_ADDR:       dc.l $24000000

LOOP_COUNTER:           dc.l $20000000+loopCounter
VBLANK_COUNTER:         dc.l $20000000+loopCounter+4

;DMA control code. R2R (RAM to RAM) DMA supports copying data in 2 byte, 4 byte, and 16 byte (using 2 channels) blocks. I... haven't been able to find a lot of info on this. -_-
